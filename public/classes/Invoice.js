export class Invoice {
    constructor(client, details, amount) {
        this.client = client;
        this.details = details;
        this.amount = amount;
        this.bank = 'Bank of America';
    }
    format() {
        return `shortInvoice: ${this.client} owes $${this.amount} for ${this.details}`;
    }
}
